def addition(num1, num2)
  puts "#{num1}" 
  puts "#{num2}"
  sum = num1 + num2 
  puts = sum 
  sum 
end

def subtraction(num1, num2)
  puts "#{num1}"
  puts "#{num2}"
  subtract = num1 - num2 
  puts = subtract
  subtract
end

def division(num1, num2)
  puts "#{num1}"
  puts "#{num2}"
  division = num1 / num2 
  puts = division 
  division 
end

def multiplication(num1, num2)
  puts "#{num1}"
  puts "#{num2}"
  multiply = num1 * num2
  puts multiply 
  multiply
end

